/*
 * Returns a number --> 'java.lang.Double' in Java
 */
function add(a, b) {
	return a + b ;
}

/*
 * Returns a number --> 'java.lang.Double' in Java
 */
function modulo10(a) {
	return a % 10 ;
}

/*
 * Returns a boolean --> 'java.lang.Boolean' in Java
 */
function isOK(s) {
	if ( s == "AAA" ) {
		return true ;
	}
	else {
		return false ;
	}
}

/*
 * Returns the original Java type --> 'java.lang.Integer'
 */
function id(person) {
	return person.id;
}